// Helper to get wines by tag
export function getWinesByTag(tag: string): InventoryItem[] {
  return inventory.filter(wine => wine.tags.includes(tag));
}

// Helper to get wines by region
export function getWinesByRegion(region: string): InventoryItem[] {
  return inventory.filter(wine =>
    wine.region.toLowerCase().includes(region.toLowerCase()) ||
    wine.country.toLowerCase().includes(region.toLowerCase())
  );
}

// Helper to get wines in price range
export function getWinesInPriceRange(min: number, max: number): InventoryItem[] {
  return inventory.filter(wine => wine.price_gbp >= min && wine.price_gbp <= max);
}

// Helper to get wines ready to drink
export function getReadyToDrink(): InventoryItem[] {
  const currentYear = new Date().getFullYear();
  return inventory.filter(wine =>
    wine.drink_window_start <= currentYear &&
    wine.drink_window_end >= currentYear
  );
}

// Helper to get investment-grade wines
export function getInvestmentGrade(): InventoryItem[] {
  return inventory.filter(wine =>
    wine.tags.includes('investment') ||
    (wine.scarcity_level === 'Ultra' && wine.critic_signal >= 96)
  );
}
